#!perl

# Generate holonomic recurrences for (n+k) * n!
# @(#) $Id$
# 2021-01-31, Georg Fischer; SBW
#
#:# Usage:
#:#   perl gen_mul_fact.pl > hr.gen
#
# a(n) = (n+k) * n! = (n+k) * n * (n-1)!
# a(n-1) = (n-1+k) * (n-1)!
# (n-1)! = a(n) / (n^2 + k*n) = a(n-1) / (n-1+k)
# a(n)*(n+k-1) - a(n-1)*(n^2 + k*n) = 0
# MATRIX="[[0],[0,-k,-1],[k-1,1]]" INIT="[k,k+1]"

# n = 0: k * 0! = k
# n = 1: (1 + k) * 1! = 1 + k
#---------------------------------
use strict;
use integer;
use warnings;

while (<DATA>) {
    s{\s+\Z}{};
    my $line = $_;
    my ($aseqno, $k, $init, @rest) = split(/\t/, $line);
    if ($aseqno =~ m{\AA}) {
        print join("\t", $aseqno, "holos", 0, "[[0],[0," . (-$k) . ",-1],[" . ($k-1) . ",1]]", $init, 0, 0) . "\n";
    }
} # while <>    
