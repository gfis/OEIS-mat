#!perl

# Cut multiple equations into several lines
# @(#) $Id$
# 2024-02-02, Georg Fischer
#
#:# Usage:
#:#   perl cuteqs.pl input.cat25 > output.seq4
#--------------------------------------------------------
use strict;
use integer;
use warnings;
my ($sec, $min, $hour, $mday, $mon, $year, $wday, $yday, $isdst) = localtime (time);
my $timestamp = sprintf ("%04d-%02d-%02d %02d:%02d", $year + 1900, $mon + 1, $mday, $hour, $min);
my $progname = $0;
print "# Generated by $progname at $timestamp\n";

my $debug = 0;
while (scalar(@ARGV) > 0 and ($ARGV[0] =~ m{\A[\-\+]})) {
    my $opt = shift(@ARGV);
    if (0) {
    } elsif ($opt  =~ m{d}) {
        $debug     = shift(@ARGV);
    } else {
        die "invalid option \"$opt\"\n";
    }
} # while $opt

my %known = ();
while(<DATA>) {
    s/\s+\Z//; # chompr 
    if (length($_) > 0) {
        my ($list, $aseqno) = split(/   */); # at least 2 spaces
        $known{$list} = $aseqno;
    }
} # while DATA

my ($aseqno, $list1, $list2, $rest, $seq1, $seq2);
while (<>) {
    s/\s+\Z//; # chompr
    tr /\[\]/\(\)/; # normalize [ ] to ( )
    my $line = $_;
    if ($line =~ m{\A(A\d+)\s}) {
        $aseqno = $1;
    }
    #               (1      1 )          (2      2 )3  3
    if ($line =~ m{\(([^\)]+)\) *DELTA *\(([^\)]+)\)(.*)}) {
        ($list1, $list2, $rest) = ($1, $2, $3);
        $seq1 = &find_seq($list1);
        $seq2 = &find_seq($list2);
        print join("\t", $aseqno, "deldel", 0, "new $seq1", "new $seq2", "[$list1]", "[$list2]", $rest) . "\n";
    } else {
        print "# ?? $line\n";
    }
} # while <>
#----
sub find_seq {
    my ($list) = @_;
    $list =~ s{ }{}g;
    $list =~ s{\, *\.*\Z}{};
    my @keys = grep { m{\A$list} } keys(%known);
    my $keyno = scalar(@keys);
    my $result = "Z000000";
    if (0) {
    } elsif ($keyno == 0) {
        print STDERR " ,$list\n";
    } elsif ($keyno == 1) {
        $result = $known{$keys[0]};
    } else {
        print STDERR "# $0, several entries for $list: " . join(",", map { $known{$_} } @keys) . "\n";
    }
    return $result;
} # find_seq
