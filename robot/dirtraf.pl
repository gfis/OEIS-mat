use strict;
use integer;
use warnings;
my ($sec, $min, $hour, $mday, $mon, $year, $wday, $yday, $isdst) = localtime (time);
my $timestamp = sprintf ("%04d-%02d-%02d %02d:%02d", $year + 1900, $mon + 1, $mday, $hour, $min);
my $progname = $0;
print "# Generated by OEIS-mat/robot/$progname at $timestamp\n";

my $dirfile = "direct.txt";

my $debug = 0;
if (0 && scalar(@ARGV) == 0) {
    print `grep -E "^#:#" $0 | cut -b3-`;
    exit;
}
while (scalar(@ARGV) > 0 and ($ARGV[0] =~ m{\A[\-\+]})) {
    my $opt = shift(@ARGV);
    if (0) {
    } elsif ($opt  =~ m{d}) {
        $debug     = shift(@ARGV);
    } elsif ($opt  =~ m{f}) {
        $dirfile   = shift(@ARGV);
    } else {
        die "invalid option \"$opt\"\n";
    }
} # while $opt

my %dirs = ();
my $count = 0;
open(DIR, "<", $dirfile) || die "** cannot read \"$dirfile\"\n";
while (<DIR>) {
    my $dirl = $_;
    #                1 as 1  2 ofs1 2
    if ($dirl =~ m{\A(A\d+)\t(\-?\d+)}) {
        $dirs{$1} = $2;
        $count ++;
    }
} # while <DIR>
close(DIR);
print STDERR "# $count DirectSequences read\n";

while (<>) {
    next if ! m{\AA\d\d+};
    s{\s+\Z}{}; # chompr
    my ($aseqno, $callcode, $offset, $expr, @rest) = split(/\t/);
    #              1    1  2    2
    if ($expr =~ m{(A\d+)\((A\d+)\(n\)\)}) {
        my ($dseqno, $rseqno ) = ($1, $2);
        if (0) {
        } elsif (defined($dirs{$dseqno})) {
            my $offset1 = $dirs{$dseqno};
            print        join("\t", $aseqno, "dirtraf", 0, "new $dseqno()", "new $rseqno()", "", "") ."\n";
        } else {
            print STDERR join("\t", $aseqno, "nodir"  , 0, "new $dseqno()", "new $rseqno()", "", "$expr") ."\n";
        }
    } # foreach
} # foreach $line
